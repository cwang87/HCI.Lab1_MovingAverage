package lab1.cwang87.lab1;


public class Joy {
    private float joyData;
    private float timeStamp;

    public Joy(float joyData, float timeStamp) {
        this.joyData = joyData;
        this.timeStamp = timeStamp;
    }

    public float getJoyData() {
        return joyData;
    }

    public float getTimeStamp() {
        return timeStamp;
    }
}
